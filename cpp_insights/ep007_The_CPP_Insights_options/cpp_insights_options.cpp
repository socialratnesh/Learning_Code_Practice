/*********
  This example demonstrates about the C++ insights options
  - for-loops as while-loops
  - array subscription

 **********/

int main() {

	for(int i = 0; i < 5; ++i) { }

	char buffer[10] {};
	for(int i = 0; i < 5; ++i) { 
		buffer[i] = i;
	}

	int arr[4] {};
	arr[2] = 5;

	return 0;
}

